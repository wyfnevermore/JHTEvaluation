package com.chinaias.entity;

import javax.persistence.Entity;
import javax.persistence.Id;
import javax.persistence.Table;

/** 半成品 */
@Entity
@Table(name = "EV_HALFPRODUCT", schema = "jhteval")
public class HalfProduct {
   /** id*/
   public String id;
   /** 清单号*/
   public String listID;
   /** 规格*/
   public String spec;
   /** 分类 */
   public String classify;
   /** 半成品名称 */
   public String hpName;
   /** 物料描述*/
   public String materailDesc;
   /** 裁片长*/
   public float cutLength;
   /** 裁片宽 */
   public float cutWidth;
   /** 裁片宽高 */
   public float cutHeight;
   /** 裁片其他 */
   public float cutOther;
   /** 耗材 */
   public float materialLoss;
   /** 衍缩 */
   public float shrink;
   /** 梳理损耗 */
   public float combingLoss;
   /** 原材料单耗 */
   private float materialUnitLoss;
   /** 成本 */
   public float cost;
   /** 备注 */
   public String remark;
   /** 工厂*/
   public String factory;
   /** 选择物料的json */
   public String selMaterialJson;
   /** 计算公式表达式*/
   public String formulaExpress;
   /** 计算公式值*/
   public String formulaVal;
   /** 规格其他 */
   private float specOther;
   /** 加工费json*/
   private String processCostJson;
   
   @Id
   public String getId() {
      return id;
   }
   
   public void setId(String newId) {
      id = newId;
   }
   
   public String getListID() {
      return listID;
   }
   
   public void setListID(String newListID) {
      listID = newListID;
   }
   
   public String getSpec() {
      return spec;
   }
   
   public void setSpec(String newSpec) {
      spec = newSpec;
   }
   
   public String getClassify() {
      return classify;
   }
   
   public void setClassify(String newClassify) {
      classify = newClassify;
   }
   
   public String getHpName() {
      return hpName;
   }
   
   public void setHpName(String newHpName) {
      hpName = newHpName;
   }
   
   public String getMaterailDesc() {
      return materailDesc;
   }
   
   public void setMaterailDesc(String newMaterailDesc) {
      materailDesc = newMaterailDesc;
   }
   
   public float getCutLength() {
      return cutLength;
   }
   
   public void setCutLength(float newCutLength) {
      cutLength = newCutLength;
   }
   
   public float getCutWidth() {
      return cutWidth;
   }
   
   public void setCutWidth(float newCutWidth) {
      cutWidth = newCutWidth;
   }
   
   public float getCutHeight() {
      return cutHeight;
   }
   
   public void setCutHeight(float newCutHeight) {
      cutHeight = newCutHeight;
   }
   
   public float getCutOther() {
      return cutOther;
   }
   
   public void setCutOther(float newCutOther) {
      cutOther = newCutOther;
   }
   
   public float getMaterialLoss() {
      return materialLoss;
   }
   
   public void setMaterialLoss(float newMaterialLoss) {
      materialLoss = newMaterialLoss;
   }
   
   public float getShrink() {
      return shrink;
   }
   public void setShrink(float newShrink) {
      shrink = newShrink;
   }
   
   public float getCombingLoss() {
      return combingLoss;
   }
   
   public void setCombingLoss(float newCombingLoss) {
      combingLoss = newCombingLoss;
   }
   
   public float getCost() {
      return cost;
   }
   
   public void setCost(float newCost) {
      cost = newCost;
   }
   
   public String getRemark() {
      return remark;
   }
   
   public void setRemark(String newRemark) {
      remark = newRemark;
   }
   
   public String getFactory() {
      return factory;
   }
   
   public void setFactory(String newFactory) {
      factory = newFactory;
   }
   
   public String getSelMaterialJson() {
      return selMaterialJson;
   }
   
   public void setSelMaterialJson(String newSelMaterialJson) {
      selMaterialJson = newSelMaterialJson;
   }
   
   public String getFormulaExpress() {
      return formulaExpress;
   }
   
   public void setFormulaExpress(String newFormulaExpress) {
      formulaExpress = newFormulaExpress;
   }
   
   public String getFormulaVal() {
      return formulaVal;
   }
   
   public void setFormulaVal(String newFormulaVal) {
      formulaVal = newFormulaVal;
   }

public float getMaterialUnitLoss() {
	return materialUnitLoss;
}

public void setMaterialUnitLoss(float materialUnitLoss) {
	this.materialUnitLoss = materialUnitLoss;
}

public float getSpecOther() {
	return specOther;
}

public void setSpecOther(float specOther) {
	this.specOther = specOther;
}

public String getProcessCostJson() {
	return processCostJson;
}

public void setProcessCostJson(String processCostJson) {
	this.processCostJson = processCostJson;
}

}